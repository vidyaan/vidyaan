public class DataTypes {
    public static void main(String[] args) {
        int age = 24;
        long phone = 8562412456L;

        float height = 85.35f;
        double pi = 3.142125;

        char section = 'A';
        String name = "Akshay Rao";
        boolean isTrainer = true;


        System.out.println("Name (String): " + name);
        System.out.println("Favourite Constant (Pi - Double Float): " + pi);

        System.out.println("Age (Integer): " + age);
        System.out.println("Height (Float): " + height);
        System.out.println("Section (Character): " + section);
        System.out.println("Are you a Trainer? (Boolean): " + isTrainer);
        System.out.println("Phone Number (Long Integer): " + phone);

    }
}
