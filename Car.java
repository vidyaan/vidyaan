public class Car{
    public String name, fuel;
    private double price;
    private String brand;


    // Setters
    public void setBrand(String Brand) {
        brand = Brand;
    }


    // Getters
    public String getBrand() {
        return brand;
    }


    // Setters
    public void setPrice(double Price) {
        price = Price;
    }


    // Getters
    public double getPrice() {
        return price;
    }
}
